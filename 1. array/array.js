const arr = [1, 2, 3, "Abhishek"];
arr.push(4); // Insert an element at last index
arr.unshift(0); // Insert an element at first index
arr.pop(); // Remove an element from last index
arr.shift(); // Remove an element from first index

for(const item of arr) {
    console.log(item);
}

